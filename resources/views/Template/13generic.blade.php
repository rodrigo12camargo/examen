							<h1>T13 Tipos de servidores</h1>
							<span class="image main"></span>
                            <h2>Servidor web</h2>  
							<p>Es la maquina que almacena una o varias paginas dependiendo de si es un servidor web dedicado o compartido </p>
                            <h2>Servidor dedicado</h2>
							<p>Este tipo de servidores se caracterizan por recibir peticiones de un único cliente, estos son exclusivos y que, por tanto, suelen ofrecer mayor cantidad de almacenamiento. </p>
							<p>Este tipo de servidores son los que ofrecen algunas empresas de hosting para aquellos clientes que tienen una pagina web que requieren volumen de transferencia de datos muy elevado. </p>
                            <h2>Servidor compartido</h2> 
                            <p>Es lo apuesto al dedicado. Estos se encargan de recibir peticiones de muchos clientes. Por lo general los clientes de este tipo de servidores son aquellos que no realizan muchas peticiones al servidor ya que, de no ser así, estos no serían lo suficiente rápidos para adaptarse a las necesidades de todos sus clientes. </p>
                            <h2> De audio y video</h2> 
                            <p>Son muy útiles para la transmisión de audios y videos en streaming, estos son creados para esta tarea para esta tarea en concreto ya que la transmisión en directa de audio es una tarea que requiere de un servidor que sea potente </p> 
                            <h2>De correo electronico </h2> 
                            <p> Encargados de gestionar todos los emails que reciben y envían un correo electrónico, además es el responsable de todas las operaciones de dicho email y permite el intercambio de mensajes a través de sistemas de comunicaciones electrónicos.  </p>
                            <h2>Servidor FTP</h2> 
                            <p>Es un tipo de servidor que permite la transferencia de archivos, es muy propio dentro del diseño web ya que permite al cliente descargar un archivo desde el servidor o al servidor recibir un archivo desde el cliente.</P> 
                            <h2>Servidor de impresion</h2>
                            <p>Con la función de vincular una PC a una o varias impresoras. Este tipo de servicios es ideal en entornos de trabajo donde el volumen de impresión es grande. Por tanto, un equipo de trabajo puede solicitar un trabajo de impresión a la impresora que se desee. </p>
                            <h2>Servidor en la nube</h2> 
                            <p> Estos se caracterizan por almacenar informacion en internet, en vez de en algún equipo informático físico. Hay algunas plataformas como GOOGLE DRIVE o  DROPBOX que son herramientas para que se guarde la información en la nube. De esta forma se puede acceder a la informacion siempre que tengas internet. </p> 
                            <h2>De bases de datos </h2> 
                            <p>Estos servidores se encargan de transferir información almacenada en bases de datos a otros programas u ordenadores. De esta manera, organiza la información mediante el uso de tablas, índices y registros. Es muy propio de empresas o bancos que tienen varios clientes con información detallada de cada uno de ellos como: correo, dirección, teléfono, etc. </p> 
                            <h2> Cluster de servidores </h2> 
                            <p> Esto más que un servidor, es la agrupación de varios servidores. Su particularidad es que todos se dedican a la misma tarea y se suele recurrir a esta unión cuando un único servidor no responde al número de peticiones que hacen los clientes. </p>
                            <h2>Servidor proxy </h2> 
                            <p> Un servidor proxy es un equipo informático que actúa de intermediario entre un dispositivo y otro servidor del cual un cliente solicita un servicio. Un ejemplo es la navegación de internet en un ordenador y la propia red de internet. De esta manera, el proxy es quien recibe la petición de acceder a una página y es quien trasmite la petición al servidor web.</p> 
                            <h2> Servidor de archivos</h2> 
                            <p>Los serviodres de archivos tienen como finalidad crear una red en la que varios ordernadores se pueden conectar y ver y modificar todos los archivos que hay en dicha red. esta es una practica muy utilizada en equipos de trabajo que necesitan acceso a los mismos archivos</p>
                            <h2>Servidor de DNS</h2> 
                            <p>La función de este servidor es la de traducir la dirección IP de una página web en un dominio. La razón de este cambio es porque resulta más fácil recordar los dominios, como “desafiohosting.com” antes que una dirección IP numérica como “…”</p> 

