							<h2>Tipos de protocolos de red</h2>
							<p>Los protocolos para la transmisión de datos en internet más importantes son TCP (Protocolo de Control de Transmisión) e IP (Protocolo de Internet). De manera conjunta (TCP/IP) podemos enlazar los dispositivos que acceden a la red, algunos otros protocolos de comunicación asociados a internet son POP, SMTP y HTTP.</p>
							<p>Estos los utilizamos prácticamente todos los días, aunque la mayoría de los usuarios no lo sepan ni conozcan su funcionamiento. Estos protocolos permiten la transmisión de datos desde nuestros dispositivos para navegar a través de los sitios, enviar correos electrónicos, escuchar música online, etc.</p>
							<h3>Existen varios tipos de protocolos de red:</h3>
							<p>- Protocolos de comunicación de red: protocolos de comunicación de paquetes básicos como TCP / IP y HTTP.</p>
							<p>- Protocolos de seguridad de red: implementan la seguridad en las comunicaciones de red entre servidores, incluye HTTPS, SSL y SFTP.</p>
							<p>- Protocolos de gestión de red: proporcionan mantenimiento y gobierno de red, incluyen SNMP e ICMP.</p>
							<p>Un grupo de protocolos de red que trabajan juntos en los niveles superior e inferior comúnmente se les denomina familia de protocolos.</p>
							<p>El modelo OSI (Open System Interconnection) organiza conceptualmente a las familias de protocolos de red en capas de red específicas. Este Sistema de Interconexión Abierto tiene por objetivo establecer un contexto en el cual basar las arquitecturas de comunicación entre diferentes sistemas.</p>
							<p>A continuación listamos algunos de los protocolos de red más conocidos, según las capas del modelo OSI:</p>
							<h2>Protocolos de la capa 1 - Capa física</h2>
							<p>- USB: Universal Serial Bus</p>
							<p>- Ethernet: Ethernet physical layer</p>
							<p>- DSL: Digital subscriber line</p>
							<p>- Etherloop: Combinación de Ethernet and DSL</p>
							<p>- Infrared: Infrared radiation</p>
							<p>- Frame Relay</p>
							<p>- SDH: Jerarquía digital síncrona</p>
							<p>- SONET: Red óptica sincronizada</p>
							<h2>Protocolos de la capa 2 - Enlace de datos</h2>
							<p>- DCAP: Protocolo de acceso del cliente de la conmutación de la transmisión de datos</p>
							<p>- FDDI: Interfaz de distribución de datos en fibra</p>
							<p>- HDLC: Control de enlace de datos de alto nivel</p>
							<p>- LAPD: Protocolo de acceso de enlace para los canales</p>
							<p>- PPP: Protocolo punto a punto</p>
							<p>- STP (Spanning Tree Protocol): protocolo del árbol esparcido</p>
							<p>- VTP VLAN: trunking virtual protocol para LAN virtual</p>
							<p>- MPLS: Conmutación multiprotocolo de la etiqueta</p>
							<h2>Protocolos de la capa 3 - Red</h2>
							<p>- ARP: Protocolo de resolución de direcciones</p>
							<p>- BGP: Protocolo de frontera de entrada</p>
							<p>- ICMP: Protocolo de mensaje de control de Internet</p>
							<p>- IPv4: Protocolo de internet versión 4</p>
							<p>- IPv6: Protocolo de internet versión 6</p>
							<p>- IPX: Red interna del intercambio del paquete</p>
							<p>- OSPF: Abrir la trayectoria más corta primero</p>
							<p>- RARP: Protocolo de resolución de direcciones inverso</p>
							<h2>Protocolos de la capa 4 - Transporte</h2>
							<p>- IL: Convertido originalmente como capa de transporte para 9P</p>
							<p>- SPX: Intercambio ordenado del paquete</p>
							<p>- SCTP: Protocolo de la transmisión del control de la corriente</p>
							<p>- TCP: Protocolo del control de la transmisión</p>
							<p>- UDP: Protocolo de datagramas de usuario</p>
							<p>- iSCSI: Interfaz de sistema de computadora pequeña de Internet iSCSI</p>
							<p>- DCCP: Protocolo de control de congestión de datagramas</p>
							<h2>Protocolos de la capa 5 - Sesión</h2>
							<p>- NFS: Red de sistema de archivos</p>
							<p>- SMB: Bloque del mensaje del servidor</p>
