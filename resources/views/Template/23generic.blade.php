							<h3>El perfil Full Stack</h3>
							<p>El creador Full Stack pertenece a los expertos en tecnología más demandados por las compañías. Su particularidad es que hablamos de un perfil híbrido, pues puede desempeñarse tanto en el front-end (parte visual) como en el back-end (parte lógica) de un desarrollo web. Un Full Stack Developer cuenta con una faceta integral y tiene conocimientos y herramientas que le permiten hacer frente cualquier persona de los periodos de un plan web.</p>
							<h3>Front End</h3>
							<p>Front End es la porción de un cibersitio o aplicación web con la que los usuarios interactúan de forma directa. Y se refiere a todos los puntos visibles que son parte del plan, o sea, el contenido que el cliente ve. Incluye textos, gráficos, efectos visuales, y recursos que permiten navegar de manera funciona en un ámbito web. </p>
							<h3>Back End</h3>
							<p>Back End involucra el desarrollo Web en el campo del servidor, y procesa toda la información recibida por medio del front-end para que funcione la aplicación web. A diferencia del Front End, aquí, los usuarios no poseen contacto.</p>
