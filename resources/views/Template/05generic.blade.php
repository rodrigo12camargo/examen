							<span class="image main"></span>
							<h2>Conexión libre</h2>
							<p>Hace más de 10 años que las computadoras escritorio aparecieron de manera masiva. Esto permitió que parte apreciable de la carga de trabajo de cómputo tanto en el ámbito de cálculo como en el ámbito de la presentación se lleven a cabo desde el escritorio del usuario. En muchos de los casos el usuario obtiene la información que necesita de alguna computadora de servicio. Estas computadoras de escritorio se conectan a las computadoras de servicio empleando software que permite la emulación de algún tipo de terminal. En otros de los casos se les transfiere la información haciendo uso de recursos magnéticos o por trascripción.</p>
							<h2>Interconexión de redes</h2>
							<p>La interconexión es el despliegue de puntos de intercambio de tráfico que integran conexiones directas y privadas entre participantes. La interconexión se da en Centros de Datos independientes del operador de la red.</p>
							<p>La conexión "inter" habla de una conectividad que involucra varias partes a la vez. La interconexión vincula numerosas entidades, capaces de conectarse como una unidad con otras entidades individuales.</p>
							<p>Reunir a todos los jugadores interconectados (Nubes, datos, empresas, ecosistemas digitales o individuos) es solo una característica. Estas conexiones deben estar tan cerca una de la otra, como sea posible. La distancia crea latencia y reducir la distancia entre los distintos jugadores es la única forma de poder minimizarla.</p>
							<p>Las conexiones más cercanas (a través de conexiones cruzadas o puertos de intercambio) suelen ser mucho menos costosas que las conexiones por medio de redes de larga distancia. Los IXP (Internet Exchange Point) juegan un rol crucial en la interconexión global, son una infraestructura física por la cual los Proveedores de Servicios de Internet intercambian información y tráfico de Internet entre sus redes.</p>
							<p>Esta instalación reduce la porción del tráfico de un ISP (Internet Service Provider) que debe ser entregado hacia su proveedor de conectividad, lo que reduce el costo promedio de la entrega de su servicio. </p>
							<p>Finalmente, las empresas, las Nubes y las comunidades digitales necesitan un lugar donde reunirse para estar lo suficientemente cerca unas de otras para crear innumerables conexiones directas. Y estos lugares deben estar cerca de centros de población densos o una alta latencia degradará la experiencia del usuario, estos lugares son los Centros de Datos.</p>
							
