							<p>Es una arquitectura de paso de mensajes, que puede operar cambiantes compartidas por medio de un documento público de ingreso remoto (tipo CGI-BIN o MIME); es una solución bastante socorrida puesto que su propiedad de repartición de trabajo aprovecha la versatilidad de que un servidor puede además ser comprador de otros servidores. La entrada a los terceros recursos se hace por medio de el intermedio del servidor, quien se convierte en comprador  de otros. El número de encadenamiento de clientes-servidores se define como Modelo de N capas. El siguiente diagrama muestra un modelo cliente-servidor de 2 capas, el típico esquema de Web Hosting con diversos servidores en una sola máquina, para el servicio HTTP en Internet; los servidores conforman una linea frontal en la que si una súplica no podría ser atendida, se lleva al servidor contiguo. Para esto se utiliza el modelo común de 3 capas, ejemplificando el servidor HTTP-PHP-SQL. Balance de carga hacia los consumidores o hacia el servidor, por lo que debería entenderse que el Modelo de Balance de Carga entre consumidores y servidor es libre del modelo de N capas.</p> 
