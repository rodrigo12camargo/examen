							<h2>Computadora central</h2>
							<p>Una unidad central (en inglés mainframe), es una computadora utilizada principalmente por grandes organizaciones para aplicaciones críticas, procesamiento de datos masivos (como censos y estadísticas de la industria y del consumidor, planificación de recursos empresariales y transacciones a gran escala procesamiento). Una computadora central es más grande y tiene más potencia de procesamiento que algunas otras clases de computadoras, como miniordenadores, servidores, estaciones de trabajo y computadoras personales. </p>
							<p>La mayoría de las arquitecturas de sistemas informáticos a gran escala se establecieron en la década de 1960, pero continúan evolucionando. Las computadoras mainframe se utilizan a menudo como servidores.</p>
							<p>Una unidad central puede funcionar durante años sin problemas ni interrupciones y las reparaciones de la misma pueden ser realizadas mientras está funcionando. A menudo, las unidades centrales soportan miles de usuarios de manera simultánea que se conectan mediante falsos terminales. Algunas unidades centrales pueden ejecutar o dar cobijo a muchos sistemas operativos y por lo tanto, no funcionan como una unidad sola, sino como varios equipos virtuales. </p>
							<p>En este papel, una unidad central por sí sola puede reemplazar docenas o cientos de pequeños equipos personales, reduciendo los costes administrativos y de gestión al tiempo que ofrece una escalabilidad y fiabilidad mucho mejor.</p>
							<p>Cuando una unidad central actúa como el centro de operaciones de muchos terminales virtuales, puede ofrecer la potencia necesaria para que dichas unidades operen de manera eficiente, pero también la flexibilidad de las redes de equipos personales.</p>
							<h2>Características de las Mainframes</h2>
							<p>- Vel. de proceso: Cientos de millones de instrucciones por segundo o más.</p>
							<p>- Tamaño: Requieren instalaciones especiales y refrigeración asistida.</p>
							<p>- Facilidad de uso: Para especialistas y/o grandes empresas como bancos.</p>
							<p>- Clientes usuales: Grandes corporaciones y gobiernos.</p>
							<p>- Penetración social: Baja.</p>
							<p>- Impacto social: Muy alto, aunque pasa inadvertido, la sociedad industrial moderna no puede funcionar sin ellas.</p>
							<p>- Parque instalado: Miles en todo el mundo.</p>
							<p>- Costo: Centenares de miles de dólares o más.</p>
							<h2>Servidor dedicado</h2>
							<p>Un servidor dedicado es un equipo informático físico que destina todos sus recursos a proporcionar información y atender las peticiones de otro ordenador (cliente) que ha contratado sus servicios. Por tanto, a diferencia de un servidor compartido, el dedicado trabaja en exclusiva para un solo cliente.</p>
							<p>Por analogía, podríamos decir que un servidor dedicado es a un ordenador lo que un chalet en la montaña a su propietario. Este último disfruta de una vivienda sin vecinos a su alrededor, de igual manera que una computadora que tenga un servidor dedicado contratado goza, en soledad, de todos los medios de éste (memoria RAM, CPU, sistema operativo, disco duro, etcétera).</p>
							<h2>Características de un servidor dedicado</h2>
							<p>- Un servidor dedicado se caracteriza, esencialmente, por ofrecer sus recursos y servicios a un único individuo, aunque también son importantes los siguientes aspectos:</p>
							<p>- Se localiza, por regla general, en un centro de datos, lugar en el que se encuentran todos los recursos que una entidad necesita para procesar su información.</p>
							<p>- Proporciona más seguridad y mayor velocidad, ya que los recursos de este gran ordenador central no se comparten con otros usuarios.</p>
							<p>- Está destinado a alojar sitios web con un elevado tráfico, siendo recomendable por tanto que las que contratan estos servicios reciban un gran número de visitas diarias.</p>
							<p>- Posee una alta cuota mensual, superando, incluso, los 100 euros al mes, ya que, entre otras cosas, es un solo cliente el que hace frente a los gastos de mantenimiento.</p>
							<p>- Cuenta, en la mayoría de los casos, con un servicio de atención disponible las 24 horas del día y los 365 días del año.</p>
							
