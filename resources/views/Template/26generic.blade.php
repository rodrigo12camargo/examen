							<h1>T26 Proceso de planificacion en multiniveles. </h1>
							<span class="image main"></span>
							<p> Al dialogar del desarrollo de aplicaciones Web resulta correcto presentarlas en las aplicaciones multinivel. Los sistemas clásicos cliente/servidor pertenecen a la categoría de las aplicaciones de 2 niveles. La aplicación se basa en el comprador en lo que la base de datos está en el servidor. En esta clase de aplicaciones el peso del cálculo recae en el comprador, en lo que el servidor hace la parte menos pesada, y aquello que los consumidores acostumbran ser máquinas menos potentes que los servidores. Además, está el problema de la actualización y el mantenimiento de las aplicaciones, debido a que las modificaciones a la misma han de ser trasladada a todos los consumidores.</p>
							<p>Para resolver dichos inconvenientes se ha desarrollado el termino de arquitecturas de 3 niveles: interfaz de presentacion, logica de alicacion y los datos</p> 
							<p> La capa intermedia es el código que el cliente invoca para recobrar los datos deseados. La capa de presentación obtiene los datos y los formatea para mostrarlos correctamente. Esta separación entre la capa de presentación y la de la lógica posibilita una gigantesca flexibilidad en el momento de edificar aplicaciones, debido a que tienen la posibilidad de tener diversos interfaces sin modificar la lógica de la aplicación.</p>
							<p> La tercera capa se basa en los datos que gestiona la aplicación. Dichos datos tienen la posibilidad de ser cualquier fuente de información como una base de datos o documentos XML.</p>
							<p> La arquitectura de las aplicaciones Web acostumbran exponer un esquema de 3 niveles. El primer grado se basa en la capa de presentación que incluye no solamente el navegador, sino además el servidor web que es el responsable de ofrecer a los datos un formato conveniente. El segundo grado está referido usualmente a cualquier tipo de programa o script. Al final, el tercer grado da al segundo los datos necesarios para su ejecución.</p>
							<p>Una aplicación Web tradicional recogerá datos del cliente (primer nivel), los enviará al servidor, que ejecutará un programa (segundo y tercer nivel) y cuya consecuencia va a ser formateado y presentado al cliente en el navegador (primer grado otra vez).</p>
							
